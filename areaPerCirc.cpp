// This program calculates an the area
// perimeter of a circle with radius 5.7cm.
#include <cmath>
#include <iostream>

int main() {
    std::cout << "If a circle has the radius of 5.7cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << M_PI * pow(5.7, 2) << " cm^2\n";
    std::cout << "Perimeter = " << 2 * M_PI * 5.7 << " cm\n";
}
